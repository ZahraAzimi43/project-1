#include <iostream>


int main() {
	char ch;
	ch=getchar();
	switch(ch){
		case 'a':;
		case'A':;
		case 'i':;
		case 'I':;
		case 'o':;
		case 'O':;
		case 'u':;
		case 'U':;
		case 'e':;
		case 'E':;
		printf("Is vowel");
		break;
		default:
		printf ("Is consonant");
		
		
	}
	return 0;
}